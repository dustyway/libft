#include "libft.h"

char	*strip_nl(char *s)
{
	int	l;

	if (s)
	{
		l = ft_strlen(s);
		if (l > 0 && s[l - 1] == '\n')
			s[l - 1] = '\0';
	}
	return (s);
}
